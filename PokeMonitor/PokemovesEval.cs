        private class PokeMS
        {
            readonly List<Moveset> movesets = new List<Moveset>();
            int maxoffense = 0;
            int maxdefense = 0;

            internal void add(int m1, int m2, int off, int def)
            {
                maxoffense = off > maxoffense ? off : maxoffense;
                maxdefense = def > maxdefense ? def : maxdefense;
                movesets.Add(new Moveset(m1, m2, off, def));
            }
            
            internal void calcGrade()
            {
                foreach(Moveset ms in movesets)
                {
                    ms.calcGrade(maxoffense, maxdefense);
                }
            }

            internal Moveset Find(int move1, int move2)
            {
                foreach(Moveset ms in movesets)
                {
                    if (ms.move_1 == move1 && ms.move_2 == move2) return ms;
                }
                return null;
            }
        }

        public static string Grade(int pokeId, int move1, int move2)
        {
            Moveset ms = pokems[pokeId - 1].Find(move1, move2);
            if (ms != null)
            {
                return "O:" + ms.offense_grade + " D:" + ms.defense_grade;
            }

            return "";
        }

        class Moveset
        {
            internal int move_1;
            internal int move_2;
            int offense;
            int defense;
            internal int offense_grade;
            internal int defense_grade;

            internal Moveset(int m1, int m2, int off, int def)
            {
                move_1 = m1;
                move_2 = m2;
                offense = off;
                defense = def;
            }

            internal void calcGrade(int maxoff, int maxdef)
            {
                offense_grade = (int) Math.Round(offense * 100.0f / maxoff, 0);
                defense_grade = (int) Math.Round(defense * 100.0f / maxdef, 0);
            }
        }

